<template>
    <div class="dashboard-content-one">
        <!-- Breadcubs Area Start Here -->
        <div class="breadcrumbs-area">
            <div class="float-right">
                <router-link 
                    :to="{ 
                        name: 'AddReceipt'
                    }" 
                    class="fw-btn-fill btn-gradient-yellow text-white"
                >Add Receipts</router-link>
            </div>
            <h3>View Receipts</h3>
            <ul>
                <li>
                    <router-link :to="{ name: 'dashboard' }">Dashboard</router-link>
                </li>
                <li>
                    <router-link :to="{ name: 'InventoryMaster' }">Inventory Master</router-link>
                </li>
                <li>View Receipts</li>
            </ul>
        </div>
        <!-- <b-form-group>
            <b-input
                type="search"
                v-model.trim="s"
                placeholder="Search ..."
            ></b-input>
        </b-form-group> -->
        <b-card
            v-if="!receipts.data"
            class="text-center h-auto"
        >
            <b-spinner label="Spinning" style="width: 48px; height: 48px;"></b-spinner>
        </b-card>
        <div
            v-if="receipts.data"
        >
            <b-card
                class="h-auto"
                v-if="!receipts.data.length"
            >
                No record(s) found.
            </b-card>
            <b-card class="h-auto"
                v-if="receipts.data.length"
            >
                <template #header>
                    <div
                        class="float-right"
                    >
                        {{ receipts.from }} - {{ receipts.to }} of {{ receipts.total }} record(s) are showing.
                    </div>
                    <!-- <a 
                        href="javascript:" 
                        class="icon icon-delete text-dark" 
                        title="Remove Selected"
                        @click="deleteMultiple"
                    ></a> -->
                    <select
                        v-model="limit"
                    >
                        <option value="10">10</option>
                        <option value="25">25</option>
                        <option value="50">50</option>
                        <option value="100">100</option>
                    </select>
                    <span>Per Page</span>
                </template>
                <div
                    class="table-responsive"
                >
                    <b-table
                        :fields="receipt.fields"
                        :items="receipts.data"
                        :foot-clone="true"
                        striped
                        bordered
                    >
                        <!-- <template #head(index)="data">
                            <label>
                                <input type="checkbox" v-model="checkAll" value="1" @click="selectAll">
                                {{ data.label }}
                            </label>
                        </template> -->
                        <template #cell(index)="data">
                            <label>
                                <!-- <input 
                                    type="checkbox"
                                    v-model="receipt.check"
                                    :value="data.item.id"
                                > -->
                                {{ data.index + receipts.from }}.
                            </label>
                        </template>
                        <template #cell(action)="data">
                            <a 
                                :href="`${baseURL}invoice/receipt/${data.item.id}`"
                                target="_blank"
                            ><i class="icon-print"></i> Print</a>
                        </template>
                        <template #cell(created_at)="data">
                            {{ data.item.created_at | formatDate }}
                        </template>
                        <template #cell(description)="data">
                            <div>
                                <router-link
                                    :to="{
                                        name: 'EditReceipt',
                                        params: {
                                            id: data.item.id
                                        }
                                    }"
                                ><i class="icon-mode_edit"></i> {{ data.item.organization_name }}</router-link>
                            </div>
                            <div>{{ data.item.name }}</div>
                        </template>
                    </b-table>
                </div>
                <pagination :data="receipts" :limit="2" @pagination-change-page="fetchReceipts"></pagination>
            </b-card>
        </div>
    </div>
</template>
<script>
import { multiple_delete_receipt, view_receipt } from '../../../../api/script'
export default {
    data () {
        return {
            s: '',
            limit: 10,
            checkAll: false,
            receipt: {
                check: [],
                fields: [
                    {
                        key: 'index',
                        label: 'Sr. No.'
                    },
                    'receipt_no',
                    {
                        key: 'sale.customer_name',
                        label: 'Customer Name',
                        sortable: true
                    },
                    {
                        key: 'sale.full_invoice_no',
                        label: 'Invoice No.'
                    },
                    {
                        key: 'payment_mode'
                    },
                    {
                        key: 'amount',
                        sortable: true
                    },
                    {
                        label: 'Date',
                        key: 'created_at',
                        sortable: true
                    },
                    {
                        key: 'action'
                    }
                ]
            },
            receipts: {}
        }
    },
    mounted () {
        this.fetchReceipts()
    },
    methods: {
        searchAfterDebounce: _.debounce(
            function () {
                this.fetchReceipts()
            }, 500
        ),
        selectAll () {
            this.receipt.check = []
            if (!this.checkAll) {
                for (let index in this.receipts.data) {
                    if(!this.receipts.data[index].stock) {
                        this.receipt.check.push(this.receipts.data[index].id)
                    }
                }
            }
        },
        fetchReceipts (page = 1) {
            this.receipts = {}
            view_receipt (`page=${page}&limit=${this.limit}&s=${this.s}`)
                .then(res => {
                    this.receipts = res.data
                })
        },
        deleteMultiple () {
            if(this.receipt.check.length) {
                swal({
                  title: "Are you sure?",
                  text: "Once deleted, you will not be able to recover this record!",
                  icon: "warning",
                  buttons: true,
                  dangerMode: true,
                })
                .then((willDelete) => {
                  if (willDelete) {
                      multiple_delete_receipt ({ids: this.receipt.check})
                        .then(res => {
                            this.$toast.success(res.data.message)
                            this.fetchReceipts(1)
                        })
                  }
                })
            } else {
                swal("Warning", "Please select at least one record to delete.", "warning")
            }
        }
    },
    watch: {
        s() {
            this.searchAfterDebounce()
        },
        limit () {
            this.fetchReceipts(1)
        }
    }
}
</script>
